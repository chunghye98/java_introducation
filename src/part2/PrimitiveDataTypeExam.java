package part2;

public class PrimitiveDataTypeExam {
    public static void main(String[] args) {
        boolean isFun = false;
        System.out.println(isFun);

        char c = 'f'; // 문자 하나를 표현
        System.out.println(c);

        int x = 59;
        System.out.println(x);

        long bing = 234234234234l;
        System.out.println(bing);

        float f = 32.4f;
        System.out.println(f);
    }
}
